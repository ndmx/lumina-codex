#!/usr/bin/env node
/**
 * lumina-evaluate — the runnable evaluation harness (Stage 3).
 *
 * Loads the registry + feedback ledger as a baseline SystemState, runs a set of
 * candidate changes through the harness, and reports each verdict (promote /
 * needs-approval / reject) plus the gate deltas that produced it. Then folds the
 * promoted candidates onto the baseline to show the new committed state.
 *
 *   node scripts/lumina-evaluate.mjs [path/to/ledger.jsonl]
 *
 * The candidates below stand in for what an agent (or proposeImprovements) would
 * hand the harness. Each `apply` is a pure transform of a COPY of state, which is
 * why a rejected candidate needs no rollback.
 */

import { readFileSync, writeFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

import {
  products,
  parseLedger,
  computeFitness,
  fitnessGate,
  checkGate,
  evaluateBatch,
  applyPromotions,
} from "../packages/lumina-system/dist/index.js";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const ledgerPath = resolve(root, process.argv[2] ?? "lumina-ledger.jsonl");
const reportPath = resolve(root, "docs/system/evaluation-report.md");

const events = parseLedger(readFileSync(ledgerPath, "utf8"));
const baseline = { products, events, values: {} };

// Helpers used by candidate transforms.
const withoutTarget = (s, kind, ref) => ({
  ...s,
  events: s.events.filter((e) => !(e.target.kind === kind && e.target.ref === ref)),
});
const withValue = (s, key, value) => ({ ...s, values: { ...s.values, [key]: value } });

const candidates = [
  {
    id: "retune-accent-default",
    proposalId: "reconsider-token:accent-primary",
    target: { kind: "token", ref: "accent-primary" },
    tier: "auto",
    describe: "Ship a default accent closer to what products actually pick (resolves the override).",
    apply: (s) => withoutTarget(s, "token", "accent-primary"),
  },
  {
    id: "canonicalize-reminder",
    proposalId: "canonicalize-concept:Reminder",
    target: { kind: "concept", ref: "Reminder" },
    tier: "review",
    describe: "Add a canonical Reminder role so products stop inventing it.",
    apply: (s) => withValue(withoutTarget(s, "concept", "Reminder"), "concept:Reminder", "defined"),
  },
  {
    id: "fix-light-contrast",
    proposalId: "fix-scheme:contrast-floor",
    target: { kind: "scheme", ref: "contrast-floor" },
    tier: "human-gated",
    describe: "Darken --ls-text-muted in light scheme to clear AA (does NOT alter the contrast-floor rule).",
    apply: (s) => withValue(withoutTarget(s, "scheme", "contrast-floor"), "--ls-text-muted-light", "#3a382f"),
  },
  {
    id: "rename-shared-space",
    proposalId: "(none)",
    target: { kind: "concept", ref: "Shared Space" },
    tier: "human-gated",
    describe: "Rename the canonical Shared Space concept to Group — mutates an invariant.",
    apply: (s) => withValue(s, "concept:Shared Space", "Group"),
  },
  {
    id: "risky-token-swap",
    proposalId: "(none)",
    target: { kind: "token", ref: "spark" },
    tier: "auto",
    describe: "A change that introduces a new failure — should be rejected by the ratchet.",
    apply: (s) => ({
      ...s,
      events: [
        ...s.events,
        {
          id: "synthetic-regression",
          at: new Date().toISOString(),
          productId: "lumina-codex",
          kind: "error",
          target: { kind: "token", ref: "spark" },
          severity: "high",
          detail: "introduced by risky-token-swap",
        },
      ],
    }),
  },
];

const gates = [
  fitnessGate(),
  checkGate("no-new-clash", (s) => !s.events.some((e) => e.kind === "clash")),
];

const results = evaluateBatch(baseline, candidates, gates);
const { promoted, state: finalState } = applyPromotions(baseline, candidates, gates);

const baseScore = computeFitness(baseline.events, baseline.products).metrics.score;
const finalScore = computeFitness(finalState.events, finalState.products).metrics.score;

const verdictMark = (v) =>
  ({ promote: "PROMOTE", "needs-approval": "NEEDS-APPROVAL", reject: "REJECT" })[v];

const now = new Date().toISOString();
const md = `# Lumina Evaluation Report

_Generated ${now} from \`${process.argv[2] ?? "lumina-ledger.jsonl"}\` · ${candidates.length} candidates · baseline fitness ${baseScore}/100._

> Auto-generated by \`scripts/lumina-evaluate.mjs\`. Do not edit by hand.

## Verdicts

${results
  .map((r) => {
    const f = r.comparisons.find((c) => c.name === "fitness");
    const delta = f ? `${f.before.score} → ${f.after.score}` : "n/a";
    return `### ${verdictMark(r.verdict)} — \`${r.candidateId}\`  \`[${r.tier}]\`

- **Target:** \`${r.target.kind}:${r.target.ref}\`
- **Fitness:** ${delta}
- **Gates:** ${r.comparisons.map((c) => `${c.name}=${c.after.passed ? "ok" : "fail"}${c.regressed ? " (regressed)" : ""}`).join(", ")}
- **Reasons:** ${r.reasons.length ? r.reasons.join("; ") : "all gates held, tier auto → promote"}`;
  })
  .join("\n\n")}

## Committed after this run

Promoted (auto-tier, no regression): ${promoted.length ? promoted.map((p) => `\`${p}\``).join(", ") : "_none_"}

Fitness: ${baseScore} → **${finalScore}** /100

---

_Ratchet rule: a candidate promotes only if every gate holds, no gate regresses, no invariant is mutated without approval, and its tier is \`auto\`. \`review\` / \`human-gated\` candidates that pass all gates land in NEEDS-APPROVAL, never auto-applied._
`;

writeFileSync(reportPath, md);

console.log(`Evaluation harness — baseline fitness ${baseScore}/100\n`);
for (const r of results) {
  const f = r.comparisons.find((c) => c.name === "fitness");
  console.log(
    `  ${verdictMark(r.verdict).padEnd(14)} ${r.candidateId.padEnd(24)} [${r.tier}]  fitness ${f ? `${f.before.score}→${f.after.score}` : "n/a"}`,
  );
  if (r.verdict !== "promote") console.log(`      ↳ ${r.reasons[0]}`);
}
console.log(`\n  Promoted: ${promoted.join(", ") || "none"}`);
console.log(`  Fitness after run: ${baseScore} → ${finalScore}/100`);
console.log(`\nReport written to docs/system/evaluation-report.md`);
